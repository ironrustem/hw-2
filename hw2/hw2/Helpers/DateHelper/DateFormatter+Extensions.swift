import Foundation

extension DateFormatter {
    static var formatddMMyyyy: String {
        return "dd.MM.yyyy hh:mm"
    }
    
    static var formatHHmmA: String {
        return "h:mm a"
    }
    
    static var formatMMMddYYY: String {
        return "MMM dd, yyyy"
    }

}
